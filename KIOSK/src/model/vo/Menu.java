package model.vo;

public class Menu {
	String menuno;
	String menuname;
	String price;
	String menucate;
	String menuimg;
	String gosu;
	String spicy;
	int price_for_sum;


	public int getPrice_for_sum() {
		return price_for_sum;
	}
	public void setPrice_for_sum(int price_for_sum) {
		this.price_for_sum = price_for_sum;
	}
	public String getGosu() {
		return gosu;
	}
	public void setGosu(String gosu) {
		this.gosu = gosu;
	}
	public String getSpicy() {
		return spicy;
	}
	public void setSpicy(String spicy) {
		this.spicy = spicy;
	}
	public String getMenuimg() {
		return menuimg;
	}
	public void setMenuimg(String menuimg) {
		this.menuimg = menuimg;
	}
	public String getMenuno() {
		return menuno;
	}
	public void setMenuno(String menuno) {
		this.menuno = menuno;
	}
	public String getMenuname() {
		return menuname;
	}
	public void setMenuname(String menuname) {
		this.menuname = menuname;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getMenucate() {
		return menucate;
	}
	public void setMenucate(String menucate) {
		this.menucate = menucate;
	}
	

}
